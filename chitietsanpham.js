var modal1 = document.getElementById("myfirstModal");
var btn1 = document.getElementById("cart-one");
var close1 = document.getElementsByClassName("close")[0];
var close_footer1 = document.getElementsByClassName("close-footer")[0];
var order1 = document.getElementsByClassName("order")[0];

btn1.onclick = function () {
  modal1.style.display = "block";
}

close1.onclick = function () {
  modal1.style.display = "none";
}

close_footer1.onclick = function () {
  modal1.style.display = "none";
}

order1.onclick = function () {
  alert("Mua hàng thành công, đơn hàng sẽ giao đến bạn theo địa chỉ đăng kí")
}

window.onclick = function (event) {
  if (event.target == modal1) {
    modal1.style.display = "none";
  }
}


